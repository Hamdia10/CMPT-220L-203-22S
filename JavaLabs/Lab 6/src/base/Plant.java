package base;

public class Plant {
    boolean hasLeaves;
    boolean hasRoots;
    double height;
    boolean hasStem;
    boolean hasBranches;
    public Plant (boolean hasLeaves, boolean hasRoots, double height, boolean hasStem, boolean hasBranches){
        this.hasLeaves = hasLeaves;
        this.hasRoots = hasRoots;
        this.height = height;
        this.hasStem = hasStem;
        this.hasBranches = hasBranches;

    }
    






    
  }
